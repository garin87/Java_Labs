public class b3Logic {
	
	public static double weightKilogram(double  weightGrams) {
		   
      return  weightGrams / 1000.0;

	}

	public static double weightCentner(double  weightGrams) {
		   
      return  weightGrams / 1000.0 / 100.0;

	}
    
    public static double weightTon(double  weightGrams) {
		   
      return  weightGrams / 1000.0 / 100.0 / 1000.0;

	}

}