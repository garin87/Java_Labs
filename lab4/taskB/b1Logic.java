public class b1Logic {
	
	public static double sizeKilobyte(double sizeByte) {
		   
      return sizeByte / 1024.0;

	}

	public static double sizeMegabyte(double sizeByte) {
		   
      return sizeByte / 1024.0 / 1024.0;

	}

	public static double sizeGigabyte(double sizeByte) {
		   
      return  sizeByte / 1024.0 / 1024.0 / 1024.0;

	}

}